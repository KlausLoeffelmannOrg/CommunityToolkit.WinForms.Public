namespace CommunityToolkit.WinForms.Controls;

/// <summary>
/// Represents the font size options for the console control.
/// </summary>
public enum FontSize
{
    /// <summary>
    /// Normal font size.
    /// </summary>
    Normal = 0,

    /// <summary>
    /// Smaller font size.
    /// </summary>
    Smaller = 1,

    /// <summary>
    /// Small font size.
    /// </summary>
    Small = 2,

    /// <summary>
    /// Larger font size.
    /// </summary>
    Larger = 3,

    /// <summary>
    /// Large font size.
    /// </summary>
    Large = 4
}
